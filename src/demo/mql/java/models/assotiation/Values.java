package demo.mql.java.models.assotiation;
import org.mql.java.models.Cardinal;
public class Values {

	private Cardinal upperValue;
	private Cardinal lowerValue;
	
	public Values() {
	}
	
	public Cardinal getUpperValue() {
		return upperValue;
	}
	public void setUpperValue(Cardinal upperValue) {
		this.upperValue = upperValue;
	}
	public Cardinal getLowerValue() {
		return lowerValue;
	}
	public void setLowerValue(Cardinal lowerValue) {
		this.lowerValue = lowerValue;
	}
	
}
